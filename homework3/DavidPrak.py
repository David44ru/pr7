def десятичное_в_пятеричное(n):
    if n == 0:
        return "0"
    
    результат = ""
    while n > 0:
        результат = str(n % 5) + результат
        n //= 5
    return результат

while True:
    try:
        десятичное_число = int(input("Введите десятичное число: "))  # Ввод числа
        if десятичное_число < 0:
            print("Пожалуйста, введите неотрицательное число.")
            continue
        
        # Переводим число в пятеричную систему
        пятеричное_число = десятичное_в_пятеричное(десятичное_число)
        
        # Выводим результат
        print(f"Число {десятичное_число} в пятеричной системе: {пятеричное_число}")
        
    except ValueError:
        print("Ошибка: пожалуйста, введите целое число.")
    
    # Спрашиваем, хочет ли пользователь продолжить
    продолжить_программу = input("Хотите продолжить? (да/нет): ").strip().lower()
    if продолжить_программу != 'да':
        break  # Выход из цикла, если пользователь не хочет продолжать

# Завершение программы
input("Нажмите Enter, чтобы выйти...")
